package checkersmain;

/**
 *
 * @author aquat
 */
public class CustomIndex {

    private final int first;
    private final int second;
    
    public CustomIndex(int val1,int val2) {
        this.first = val1;
        this.second = val2;
    }
    
    public int getFirst(){
        return this.first;
    }
    
    public int getSecond(){
        return this.second;
    }
    
}
